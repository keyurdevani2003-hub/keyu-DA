USE PRACTICE;

SELECT UPPER(NAME),
LOWER(NAME),
LEFT(NAME,2),	
RIGHT(NAME,2),
MID(NAME,2,2),
LENGTH(NAME),
FROM EMPLOYEES;
 


SELECT DATE(ORDER_DATE),
DAY(ORDER_DATE),
MONTH(ORDER_DATE),
MONTHNAME(ORDER_DATE),
YEAR(ORDER_DATE),
DAYNAME(ORDER_DATE),
DATE_ADD(ORDER_DATE,INTERVAL 5 DAY),
DATE_SUB(ORDER_DATE,INTERVAL 5 DAY)
FROM ORDERS;






-- LEFT JOIN
SELECT EMPLOYEES.EMP_ID,
EMPLOYEES.NAME,
ORDERS.ORDER_AMOUNT
FROM EMPLOYEES
LEFT JOIN ORDERS
ON EMPLOYEES.EMP_ID = ORDERS.EMP_ID



-- CROSS JOIN
-- CROSS JOIN  RAW TO RAW MULTIPLICATION 

SELECT * FROM EMPLOYEES
CROSS JOIN ORDERS;


-- SELF JOIN


SELECT 
    E.EMPLOYEE_ID,
    E.NAME AS EMPLOYEE_NAME,
    E.SALARY AS SALARY,
    E.MANAGER_ID,
    M.NAME AS MANAGER_NAME
FROM SELF_JOIN_EMPLOYEES E
LEFT JOIN SELF_JOIN_EMPLOYEES M
ON E.MANAGER_ID = M.EMPLOYEE_ID
WHERE E.SALARY> M.SALARY;